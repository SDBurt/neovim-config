-- Summary: Defines color schemes and manages color settings for the application or text editor interface.
-- This script customizes themes, syntax highlighting, and UI elements' colors
-- to improve aesthetics and readability within the development environment.

function ColorMyPencils(color)
	color = color or "catppuccin"
	vim.cmd.colorscheme(color)

	vim.api.nvim_set_hl(0, "Normal", { bg = "none" })
	vim.api.nvim_set_hl(0, "NormalFloat", { bg = "none" })

end


